    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
        String opcion = tipo.getSelectedItem().toString();
        switch (opcion){
            case "Crear":
                Date date = new Date();
                Mob mob = new Mob(Integer.valueOf(id.getText()), date, nombre.getText(), "");
                rest.addMob(mob);
                break;
            case "Eliminar":
                rest.deleteMob(id.getText());
                break;
            case "Consultar":                
                mob = rest.getMob(id.getText());
                idView.setText(String.valueOf(mob.getId()));
                fechaView.setText(mob.getFechaCreacion().toString());
                nombreView.setText(mob.getNombre());
                accionView.setText(mob.getAccion());
                break;
            case "Replicar":
                break;
            case "Restaurar":
                break;    
        }
    }//GEN-LAST:event_enviarActionPerformed

